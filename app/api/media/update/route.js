import { connectDB } from "@/lib/dbConnection";
import { catchError, isAuthenticated, response } from "@/lib/helperFuncation";
import { zSchema } from "@/lib/zodSchema";
import MediaModel from "@/models/media.model";
import { isValidObjectId } from "mongoose";

export async function PUT(request, { params }) {
  try {
    const auth = await isAuthenticated("admin");
    if (!auth.isAuth) {
      return response(false, 403, "unauthorized");
    }

    await connectDB();

    const payload = await request.json();

    const schema = zSchema.pick({
      _id: true,
      alt: true,
      title: true,
    });

    const validate = schema.safeParse(payload);
    if (!validate.success) {
      return response(false, 400, "invalid or missing filed", validate.error);
    }

    const { _id, alt, title } = validate.data;

    if (!isValidObjectId(_id)) {
      return response(false, 400, "_id is invalid");
    }

    const getMedia = await MediaModel.findById(_id);
    if (!getMedia) {
      return response(false, 404, "media not found");
    }

    getMedia.alt = alt;
    getMedia.title = title;

    await getMedia.save()

    return response(true, 201, "Media updated successfully");


  } catch (error) {
    return catchError(error);
  }
}
